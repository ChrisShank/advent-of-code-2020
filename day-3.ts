function createMap(map, [rise, run]) {
  const rows = map
    .split('\n')
    .map(row => row.split(''))

  const numberOfRows = rows.length
  const numberOfColumns = rows[0].length

  const occurances = Math.ceil((numberOfRows - 1) * run / (numberOfColumns * rise))

  return rows.map(row => {
    const newRow = []

    for (let i = 0; i < occurances; i += 1) {
      newRow.push(...row)
    }

    return newRow    
  })
}

function encoutneredTrees(input, slope) {
  const map = createMap(input, slope)
  const [rise, run] = slope
  // console.log(map.map(row => row.join('')).join('\n'))

  let x = 0
  let y = 0
  let trees = 0

  while(y <= map.length - 1) {
    if (map[y][x] === '#') {
      trees += 1
    }
    y += rise
    x += run
  }

  return trees
}

function slopeProduct(map) {
  const slopes = [
    [1, 1],
    [1, 3],
    [1, 5],
    [1, 7],
    [2, 1],
  ]

  return slopes
    .map(slope => encoutneredTrees(map, slope))
    .reduce((acc, trees) => acc * trees)
}

